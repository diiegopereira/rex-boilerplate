							<p>The Romans conquered the Greek cities in Italy around three hundred years BCE and much of the Western world after that. The Roman problem of rulership involved the unity of disparity — from Spanish to Greek, Macedonian to Carthaginian — Roman rule had extended itself across the breadth of the known world and the myriad pacified cultures forming this ecumene presented a new challenge for justice.</p>		
							<p>One way to look at the unity of Roman architecture is through a new-found realization of theory derived from practice, and embodied spatially. Civically we find this happening in the Roman forum (sibling of the Greek agora), where public participation is increasingly removed from the concrete performance of rituals and represented in the decor of the architecture. Thus we finally see the beginnings of the contemporary public square in the Forum Iulium, begun by Julius Caesar, where the buildings present themselves through their facades as representations within the space.</p>
							<blockquote>
						    	<p>The Romans widely employed, and further developed, the arch, vault and dome.</p>
						    	<small>by <cite>James Rex</cite></small>
						    </blockquote> 
							<p>As the Romans chose representations of sanctity over actual sacred spaces to participate in society, so the communicative nature of space was opened to human manipulation. None of which would have been possible without the advances of Roman engineering and construction or the newly found marble quarries which were the spoils of war; inventions like the arch and concrete gave a whole new form to Roman architecture, fluidly enclosing space in taut domes and colonnades, clothing the grounds for imperial rulership and civic order. This was also a response to the changing social climate which demanded new buildings of increasing complexity — the colosseum, the residential block, bigger hospitals and academies. General civil construction such as roads and bridges began to be built.</p>
							<img src="img/blog1.jpg" class="responsive-image" alt=""/>
							<p>The Romans widely employed, and further developed, the arch, vault and dome (see the Roman Architectural Revolution), all of which were little used before, particularly in Europe.[2] Their innovative use of Roman concrete facilitated the building of the many public buildings of often unprecedented size throughout the empire. These include Roman temples, Roman baths, Roman bridges, Roman aqueducts, Roman harbours, triumphal arches, Roman amphitheatres, Roman circuses palaces, mausolea and in the late empire also churches.</p>
							<p>Roman domes permitted construction of vaulted ceilings and enabled huge covered public spaces such as the public baths like Baths of Diocletian or the monumental Pantheon in the city of Rome.</p>
							<p>Art historians such as Gottfried Richter in the 1920s identified the Roman architectural innovation as being the Triumphal Arch and it is poignant to see how this symbol of power on earth was transformed and utilized within the Christian basilicas when the Roman Empire of the West was on its last legs: The arch was set before the altar to symbolize the triumph of Christ and the after life. It is in their impressive aqueducts that we see the arch triumphant, especially in the many surviving examples, such as the Pont du Gard, the aqueduct at Segovia and the remains of the Aqueducts of Rome itself. Their survival is testimony to the durability of their materials and design.</p>
